package fr.leod1.undercover.UndercoverWord;

public class UndercoverWordOBJ {
    private String Word1;
    private String Word2;

    public UndercoverWordOBJ(String word1, String word2) {
        Word1 = word1;
        Word2 = word2;
    }

    public String getWord1() {
        return Word1;
    }

    public void setWord1(String word1) {
        Word1 = word1;
    }

    public String getWord2() {
        return Word2;
    }

    public void setWord2(String word2) {
        Word2 = word2;
    }
}
